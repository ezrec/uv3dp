		fixupV2(&eProp)
		fixupV2(&rProp)

		if !cmp.Equal(eProp, rProp) {
			t.Errorf("aa%v: expected input printable to match expected printable!", item.AntiAlias)
			t.Logf("%+v", eProp)
			t.Logf("%+v", rProp)
		}

		rLayer := result.Layer(0)
